#include "common.h"

// Available sides
const Side LEFT (0);
const Side RIGHT(1);

/**
 * Switch from one side to the other
 */
Side reverse(const Side &side)
{
	return Side(1-side.to_numeric());
}

/**
 * Return the current time point
 */
TimePoint current_time()
{
	return boost::posix_time::microsec_clock::local_time();
}
